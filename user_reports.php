            <h1>' . htmlspecialchars($title) . '</h1>
            <p>Generated on ' . date('Y-m-d H:i:s') . '</p>
        </div>
        
        <div class="info">
            <strong>Report Period:</strong> ' . htmlspecialchars($dateFrom) . ' to ' . htmlspecialchars($dateTo) . '<br>
            <strong>Total Records:</strong> ' . count($data) . '<br>
            <strong>Generated by:</strong> ' . htmlspecialchars($_SESSION['username']) . '
        </div>';

    if (empty($data)) {
        $html .= '<p>No data found for the selected period.</p>';
    } else {
        $html .= '<table>';

        // Table headers based on report type
        switch ($reportType) {
            case 'projects':
                $html .= '<tr><th>ID</th><th>Title</th><th>Description</th><th>Status</th><th>Created At</th></tr>';
                foreach ($data as $row) {
                    $html .= '<tr>
                        <td>' . htmlspecialchars($row['id']) . '</td>
                        <td>' . htmlspecialchars($row['title'] ?? 'N/A') . '</td>
                        <td>' . htmlspecialchars(substr($row['description'] ?? '', 0, 100)) . '...</td>
                        <td>' . htmlspecialchars($row['status'] ?? 'N/A') . '</td>
                        <td>' . htmlspecialchars($row['created_at']) . '</td>
                    </tr>';
                }
                break;

            case 'messages':
                $html .= '<tr><th>ID</th><th>Name</th><th>Email</th><th>Subject</th><th>Created At</th></tr>';
                foreach ($data as $row) {
                    $html .= '<tr>
                        <td>' . htmlspecialchars($row['id']) . '</td>
                        <td>' . htmlspecialchars($row['name'] ?? 'N/A') . '</td>
                        <td>' . htmlspecialchars($row['email'] ?? 'N/A') . '</td>
                        <td>' . htmlspecialchars($row['subject'] ?? 'N/A') . '</td>
                        <td>' . htmlspecialchars($row['created_at']) . '</td>
                    </tr>';
                }
                break;

            case 'users':
                $html .= '<tr><th>ID</th><th>Username</th><th>Email</th><th>Role</th><th>Created At</th></tr>';
                foreach ($data as $row) {
                    $html .= '<tr>
                        <td>' . htmlspecialchars($row['id']) . '</td>
                        <td>' . htmlspecialchars($row['username']) . '</td>
                        <td>' . htmlspecialchars($row['email']) . '</td>
                        <td>' . ($row['is_admin'] ? 'Admin' : 'User') . '</td>
                        <td>' . htmlspecialchars($row['created_at']) . '</td>
                    </tr>';
                }
                break;

            case 'my_imports':
                $html .= '<tr><th>ID</th><th>Filename</th><th>Records</th><th>Status</th><th>Imported At</th></tr>';
                foreach ($data as $row) {
                    $html .= '<tr>
                        <td>' . htmlspecialchars($row['id']) . '</td>
                        <td>' . htmlspecialchars($row['filename']) . '</td>
                        <td>' . htmlspecialchars($row['total_records']) . '</td>
                        <td>' . htmlspecialchars($row['status']) . '</td>
                        <td>' . htmlspecialchars($row['imported_at']) . '</td>
                    </tr>';
                }
                break;
        }

        $html .= '</table>';
    }

    $html .= '
        <div class="footer">
            <p>Portfolio Email System - Report Generated Automatically</p>
        </div>
    </body>
    </html>';

    return $html;
}
