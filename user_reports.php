use Dompdf\Dompdf;
use Dompdf\Options;

$message = '';
$error = '';

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    try {
        $reportType = $_POST['report_type'] ?? '';
        $dateFrom = $_POST['date_from'] ?? '';
        $dateTo = $_POST['date_to'] ?? '';
        $userId = $_SESSION['user_id'] ?? 0;

        if (empty($reportType)) {
            throw new Exception('Please select a report type');
        }

        // Set default date range if not provided
        if (empty($dateFrom))
            $dateFrom = date('Y-m-01'); // First day of current month
        if (empty($dateTo))
            $dateTo = date('Y-m-d'); // Today

        $reportData = [];
        $reportTitle = '';

        switch ($reportType) {
            case 'projects':
                $stmt = $pdo->prepare("SELECT * FROM projects WHERE created_at BETWEEN ? AND ? ORDER BY created_at DESC");
                $stmt->execute([$dateFrom, $dateTo]);
                $reportData = $stmt->fetchAll(PDO::FETCH_ASSOC);
                $reportTitle = 'Projects Report';
                break;

            case 'messages':
                $stmt = $pdo->prepare("SELECT * FROM messages WHERE created_at BETWEEN ? AND ? ORDER BY created_at DESC");
                $stmt->execute([$dateFrom, $dateTo]);
                $reportData = $stmt->fetchAll(PDO::FETCH_ASSOC);
                $reportTitle = 'Messages Report';
                break;

            case 'users':
                if (!isAdmin()) {
                    throw new Exception('Access denied: Admin privileges required for user reports');
                }
                $stmt = $pdo->prepare("SELECT id, username, email, created_at, is_admin FROM users WHERE created_at BETWEEN ? AND ? ORDER BY created_at DESC");
                $stmt->execute([$dateFrom, $dateTo]);
                $reportData = $stmt->fetchAll(PDO::FETCH_ASSOC);
                $reportTitle = 'Users Report';
                break;

            case 'my_imports':
                $stmt = $pdo->prepare("SELECT * FROM excel_imports WHERE imported_by = ? AND imported_at BETWEEN ? AND ? ORDER BY imported_at DESC");
                $stmt->execute([$userId, $dateFrom, $dateTo]);
                $reportData = $stmt->fetchAll(PDO::FETCH_ASSOC);
                $reportTitle = 'My Data Imports Report';
                break;

            default:
                throw new Exception('Invalid report type');
        }

        // Generate PDF
        $options = new Options();
        $options->set('defaultFont', 'Arial');
        $options->set('isRemoteEnabled', true);

        $dompdf = new Dompdf($options);

        $html = generateReportHTML($reportTitle, $reportData, $dateFrom, $dateTo, $reportType);

        $dompdf->loadHtml($html);
        $dompdf->setPaper('A4', 'portrait');
        $dompdf->render();

        $filename = strtolower(str_replace(' ', '_', $reportTitle)) . '_' . date('Y-m-d_H-i-s') . '.pdf';

        // Save report record
        $stmt = $pdo->prepare("INSERT INTO pdf_reports (report_type, filename, created_by, date_from, date_to) VALUES (?, ?, ?, ?, ?)");
        $stmt->execute([$reportType, $filename, $userId, $dateFrom, $dateTo]);

        // Output PDF
        $dompdf->stream($filename, array('Attachment' => true));
        exit;

    } catch (Exception $e) {
        $error = $e->getMessage();
    }
}

function generateReportHTML($title, $data, $dateFrom, $dateTo, $reportType)
